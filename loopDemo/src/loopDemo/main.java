package loopDemo;

public class main {

	public static void main(String[] args) {
		
		//For Loop
		for (int i = 0; i <= 10; i++) {
			System.out.println(i);
		}
		System.out.println("For döngüsü bitti.");
		
		//While Loop
		int i = 1;
		while (i<=10) {
			System.out.println(i);
			i++;
		}
		System.out.println("While döngüsü bitti.");
	}

}
